                <p>
                เมื่อวันที่ ๑๔ ธันวาคม ๒๕๕๘ เวลา ๑๕.๒๐ น. สมเด็จพระเทพรัตนราชสุดา ฯ สยามบรมราชกุมารี เสด็จพระราชดำเนินโดยรถยนต์พระที่นั่ง ถึงมณฑลพิธีวางศิลาฤกษ์อาคารเรียนรวมและปฏิบัติการ 
                <br>
                คณะวิทยาศาสตร์ มหาวิทยาลัยขอนแก่น เพื่อทรงประกอบพิธีวางศิลาฤกษ์อาคารเรียนรวมและปฏิบัติการ คณะวิทยาศาสตร์ โดยมีนายประเสริฐ ลือชาชนานนท์ รองผู้ว่าราชการจังหวัดขอนแก่น 
                <br>
                ดร.ณรงค์ชัย อัครเศรณี นายกสภามหาวิทยาลัย รองศาสตราจารย์ ดร.กิตติชัย ไตรรัตนศิริชัย อธิการบดี ผู้ช่วยศาสตราจารย์ ดร.สมเกียรติ ศรีจารนัย คณบดีคณะวิทยาศาสตร์ พร้อมด้วยคณะผู้บริหาร คณาจารย์ ข้าราชการ พนักงาน และนักศึกษาเฝ้าฯรับเสด็จ โดย ผศ.รจนา บุระคำ ผู้ช่วยคณบดีฝ่ายวิจัยและวิเทศสัมพันธ์ คณะวิทยาศาสตร์ ถวายพวงมาลัยคล้องพระกร ๑ พวง นายภูมิพัฒน์ ผาดโผน 
                <br>
                นายกสโมสรนักศึกษา คณะวิทยาศาสตร์ ถวายพวงมาลัยคล้องพระกร ๑ พวง ผศ.ดร.สมเกียรติ ศรีจารนัย คณบดีคณะวิทยาศาสตร์ ถวายหนังสือสูจิบัตร การก่อสร้างอาคารเรียนรวมและปฏิบัติการ 
                <br>
                คณะวิทยาศาสตร์ ผศ.เขมิกา ลมไธสง รองคณบดีฝ่ายวิชาการ คณะวิทยาศาสตร์ ถวายผลิตภัณฑ์พื้นเมือง (ผ้าไหม) จำนวน ๑ ชิ้น นายโชติ  ตราชู นายกสมาคมศิษย์เก่า ถวายผลิตภัณฑ์พื้นเมือง (ผ้าไหม) 
                <br>
                จำนวน ๑ ชิ้น
