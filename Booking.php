                <h4>ระเบียบและวิธีการใช้ระบบงาน</h4>
                <p>
                1.ท่านจะต้องทำการล็อกอิน (Login) เข้าสู่ระบบก่อน เมื่อจะทำการจองใช้ห้อง
                <br>
                2.หากท่านยังไม่ลงทะเบียนเข้าสู่ระบบกรุณาทำการลงทะเบียน เพื่อเข้าใช้ระบบ
                <br>
                3.เมื่อทำการล็อกอิน (Login) เข้าสู่ระบบแล้ว ท่านจะต้องตรวจสอบข้อมูลก่อนทุกครั้งว่า ในวันที่ท่านต้องการใช้ห้องประชุม มีการจองใช้ห้องอยู่ก่อนแล้วหรือไม่
                <br> 
                4.ท่านจะต้องทำการกรอกข้อมูลให้ครบทุกช่องในการจอง และเข้าใช้ระบบทุกครั้ง
                <br>
                5.กรุณาอย่าส่งข้อมูลซ้ำ ในกรณีที่ห้องประชุมถูกจองพร้อมกัน จะใช้ความสำคัญของการใช้ห้องเป็นหลัก
                <br>
                6.เมื่อทำการจองห้องประชุมเสร็จแล้วกรุณาตรวจสอบสถานะการจองห้อง ภายหลังจากการจอง 1 วัน
                <br>
                7.เมื่อทำงานต่างๆเรียบร้อยแล้วกรุณาทำการล็อกเอ้าท์ (Logout) ออกจากระบบ
                <br>
                8.หากมีข้อสงสัย หรือปัญหากรุณาติดต่อเจ้าหน้าที่ระบบ
                </p>
                <h4>ขั้นตอนการใช้งานระบบจองห้องประชุม</h4>
                1.เข้าไปที่เมนูเข้าสู่ระบบ กรอก Username และ Password เเล้วกดปุ่ม Login เพื่อเข้าใช้งาน หากยังไม่มี Username 
                <br>
                เข้าใช้งานให้กดที่ปุ่ม สมัครสมาชิก
                <br>
                2.เข้าไปที่เมนูตารางการใช้ห้อง จะแสดงรายละเอียดการจองห้องประชุมในแต่ละห้อง ซึ่งจะแสดงในรูปแบบปฏิทิน 
                <br>
                ถ้าต้องการดูรายละเอียดให้กดที่ชื่อการประชุมบนปฏิทิน
                <br>
                3.เข้าไปที่เมนูจองห้องประชุมเเล้วเลือกห้องประชุมที่ต้องการเเเละกดปุ่มจองห้องประชุม
                <br>
                4.กรอกข้อมูลรายละเอียดการจองเเล้วคลิกปุ่ม Submit เพื่อบันทึก
                <br>
                5.รอการอนุมัติจากเจ้าหน้าที่ สามารถตรวจสอบ สถานะการจอง แก้ไขการจอง หรือลบรายการจองได้ที่เมนูตรวจสอบสถานะการจอง
