                <h4>หมายเหตุ</h4>
                <p>
                1.กรุณาตรวจสอบเวลาของการจองห้องก่อนยืนยัน
                <br>
                2.เพื่อให้เกิดความสะดวกในการติดต่อ กรุณาระบุเบอร์โทรศัพท์ในการติดต่อกลับกรณีเกิดปัญหาทุกครั้ง
                <br>
                3.การจองห้องเพื่อการเรียนการสอน : หากตรงกับช่วงสอบ ทางผู้ดูแลจะลำดับความสำคัญในการสอบก่อน
                <br> 
                4.การจองห้องผู้ใช้ควรตรวจสอบสถานะการจองทุกครั้ง (อนุมัติ/ไม่อนุมัติ) ก่อนเข้าใช้ห้อง
                <br>
                5.ติดต่อผู้ดูแลระบบ vsonta@kku.ac.th เบอร์โทรศัพท์ : 084-600-1149 / 091-421-0065
                </p>
