                <br>
                2.เลือกอาคารและห้อง ที่ต้องการจอง
                <br>
                3.เลือกรูปแบบการแสดงปฏิทินและเลือกช่วงเวลาการจอง
                <br>
                4.กรอกรายละเอียดการจอง/ตรวจสอบสถานะห้องที่ทำการจองและบันทึกการจอ
